    private void txtConsultarActionPerformed(java.awt.event.ActionEvent evt) {
        String idProducto =txtIDProducto.getText();
        if(idProducto.equals("")){
            JOptionPane.showMessageDialog(this,"Debe ingresas IdProducto");
            return;
        }
        Datos misDatos = new Datos();
        Producto miProducto =misDatos.getProducto(idProducto);

        if(miProducto == null){
            JOptionPane.showMessageDialog(this,"Producto no existe");
            misDatos.cerrarConexion();
            return;
        }
        misDatos.cerrarConexion();
        txtDescripcion.setText(miProducto.getDescripcion());
        txtPrecio.setText(""+ miProducto.getPrecio());
        txtNotas.setText(miProducto.getNotas());


    }

